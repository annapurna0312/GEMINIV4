    private boolean onSegment(Point p, Point q, Point r){
        if (q.x <= Math.max(p.x, r.x) && q.x >= Math.min(p.x, r.x)
                && q.y <= Math.max(p.y, r.y) && q.y >= Math.min(p.y, r.y))
            return true;
        return false;
    }
    private double orientation(Point p, Point q, Point r){
        double val = (q.y - p.y) * (r.x - q.x) - (q.x - p.x) * (r.y - q.y);
        if (val == 0)
            return 0;
        return (val > 0) ? 1 : 2;
    }
    private boolean doIntersect(Point p1, Point q1, Point p2, Point q2){
        double o1 = orientation(p1, q1, p2);
        double o2 = orientation(p1, q1, q2);
        double o3 = orientation(p2, q2, p1);
        double o4 = orientation(p2, q2, q1);
        if (o1 != o2 && o3 != o4)
            return true;
        if (o1 == 0 && onSegment(p1, p2, q1))
            return true;
        if (o2 == 0 && onSegment(p1, q2, q1))
            return true;
        if (o3 == 0 && onSegment(p2, p1, q2))
            return true;
        if (o4 == 0 && onSegment(p2, q1, q2))
            return true;
        return false;
    }
    private boolean isInside(Point polygon[], Point p){
        int n = polygon.length;
        int INF = 10000;
        if (n < 3)
            return false;
        Point extreme = new Point(INF, p.y);
        int count = 0, i = 0;
        do{
            int next = (i + 1) % n;
            if (doIntersect(polygon[i], polygon[next], p, extreme)){
                if (orientation(polygon[i], p, polygon[next]) == 0)
                    return onSegment(polygon[i], p, polygon[next]);
                count++;
            }
            i = next;
        } while (i != 0);
        return (count & 1) == 1 ? true : false;
    }

    public void getNearestPointDetails(Point polygon[], Point p, char distancemeasure, IndianBoundaryDetails retMap2){
        Point mindistpoint = null;
        double distance = 0.0;
        for(Point regionpoint : polygon) {
            if(mindistpoint == null) {
                mindistpoint = regionpoint;
                distance = distance(regionpoint.x, regionpoint.y, p.x, p.y, distancemeasure);
            }else {
                double dist = distance(regionpoint.x, regionpoint.y, p.x, p.y, distancemeasure);
                if( dist < distance ) {
                    distance = dist;
                    mindistpoint = regionpoint;
                }
            }
        }
        retMap2.setDistance(new Converter().roundto2DecimalPlaces(distance));
        retMap2.setClat(mindistpoint.x);
        retMap2.setClon(mindistpoint.y);
    }


    private double distance(double lat1, double lon1, double lat2, double lon2, char unit) {
        double theta = lon1 - lon2;
        double dist = Math.sin(deg2rad(lat1)) * Math.sin(deg2rad(lat2)) + Math.cos(deg2rad(lat1)) * Math.cos(deg2rad(lat2)) * Math.cos(deg2rad(theta));
        dist = Math.acos(dist);
        dist = rad2deg(dist);
        dist = dist * 60 * 1.1515;
        if (unit == 'K') {
            dist = dist * 1.609344;
        } else if (unit == 'N') {
            dist = dist * 0.8684;
        }
        return (dist);
    }


