    public void getNearestPointDetails(Point polygon[], Point p, char distancemeasure, NearestCountry nearestCountry){
        Point mindistpoint = null;
        double distance = 0.0;
        for(Point regionpoint : polygon) {
            if(mindistpoint == null) {
                mindistpoint = regionpoint;
                distance = distance(regionpoint.x, regionpoint.y, p.x, p.y, distancemeasure);
            }else {
                double dist = distance(regionpoint.x, regionpoint.y, p.x, p.y, distancemeasure);
                if( dist < distance ) {
                    distance = dist;
                    mindistpoint = regionpoint;
                }
            }
        }
        nearestCountry.setDistnce(new Converter().roundto2DecimalPlaces(distance));
        nearestCountry.setClat(mindistpoint.x);
        nearestCountry.setClon(mindistpoint.y);
    }





    private double distance(double lat1, double lon1, double lat2, double lon2, char unit) {
        double theta = lon1 - lon2;
        double dist = Math.sin(deg2rad(lat1)) * Math.sin(deg2rad(lat2)) + Math.cos(deg2rad(lat1)) * Math.cos(deg2rad(lat2)) * Math.cos(deg2rad(theta));
        dist = Math.acos(dist);
        dist = rad2deg(dist);
        dist = dist * 60 * 1.1515;
        if (unit == 'K') {
            dist = dist * 1.609344;
        } else if (unit == 'N') {
            dist = dist * 0.8684;
        }
        return (dist);
    }

    /*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
    /*::  This function converts decimal degrees to radians             :*/
    /*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
    private double deg2rad(double deg) {
        return (deg * Math.PI / 180.0);
    }

    /*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
    /*::  This function converts radians to decimal degrees             :*/
    /*:::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::*/
    private double rad2deg(double rad) {
        return (rad * 180.0 / Math.PI);
    }




